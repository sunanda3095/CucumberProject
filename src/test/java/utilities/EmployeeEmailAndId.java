package utilities;

public class EmployeeEmailAndId {
    private String empId;
    private String email;

    // Getter for empId
    public String getEmpId() {
        return empId;
    }

    // Setter for empId
    public void setEmpId(String empId) {
        this.empId = empId;
    }

    // Getter for email
    public String getEmail() {
        return email;
    }

    // Setter for email
    public void setEmail(String email) {
        this.email = email;
    }
}
